import java.util.*;
import java.io.*;
public class SumOf{
  
  public static void main(String args[])
  {
    
    int n,i,sum=0;
    Scanner sc=new Scanner(System.in);
    System.out.println("enter the number");
    n=sc.nextInt();
    for(i=1;i<=n;i++)
    {
      
      sum=sum+i;
      }
      System.out.println("The sum :"+sum);
      }
      }
